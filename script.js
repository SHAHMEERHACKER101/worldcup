// 2. Authentic Countdown Timer Logic
// Counts down to the official opening match: June 11, 2026 at Estadio Azteca
const countdownEl = document.getElementById('countdown-display');
const openingMatchDate = new Date("2026-06-11T12:00:00Z").getTime();

function updateCountdown() {
    const now = new Date().getTime();
    const distance = openingMatchDate - now;

    if (distance < 0) {
        if(countdownEl) countdownEl.innerHTML = "<span style='color:var(--fifa-red); font-weight:900;'>THE TOURNAMENT HAS BEGUN!</span>";
        return;
    }

    const days = Math.floor(distance / (1000 * 60 * 60 * 24));
    const hours = Math.floor((distance % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
    const minutes = Math.floor((distance % (1000 * 60 * 60)) / (1000 * 60));
    const seconds = Math.floor((distance % (1000 * 60)) / 1000);

    if (countdownEl) {
        countdownEl.innerHTML = `
            Opening Match Kickoff In:<br>
            <span style="color:var(--fifa-gold); font-size:2rem; margin-top:10px; display:block;">
                ${days}d : ${hours.toString().padStart(2, '0')}h : ${minutes.toString().padStart(2, '0')}m : ${seconds.toString().padStart(2, '0')}s
            </span>
        `;
    }
}

// Start countdown
setInterval(updateCountdown, 1000);
